// Extract country code from creative ID (e.g., HO2TZ -> TZ)
export function getCountryCodeFromCreative(creativeId: string): string {
  // Check if the creative ID ends with a country code (2 uppercase letters)
  const match = creativeId.match(/[A-Z]{2}$/)
  if (match) {
    const countryCode = match[0]
    // Check if this is a valid country code in our map
    if (countryData[countryCode]) {
      return countryData[countryCode].name
    }
  }
  return ""
}
